&ldquo;innovation&rdquo;. When the head of Quality Control(QC)
refused to sign off on the submersible unless and until non
destructive testing had been done. The CEO gave him ten minutes to
clear his desk and fired him on the spot. Court cases followed,suing
and counter suing. There was a final undisclosed settlement out of
court. The QC guy had done his best. He could do no more. 
